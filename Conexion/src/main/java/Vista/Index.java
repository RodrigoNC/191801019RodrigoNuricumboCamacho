    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombreUsuario = jTextField1.getText();
        String contrasena = jTextField2.getText();
        Usuarios usuario = new Usuarios(nombreUsuario, contrasena);
        try {
            DaoUsuarios.insertar(usuario);
        } catch (SQLException ex) {
            Logger.getLogger(Index.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            jTextField1.setText(" ");
            jTextField2.setText(" ");
            try{
                    this.crearModelo();
                } catch (SQLException ex){
                    Logger.getLogger(Index.class.getName()).log(Level.SEVERE, null, ex);
                }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        if(evt.getClickCount()==2){
            int index = jList1.getSelectedIndex();
            Usuarios usuario = (Usuarios)modelo.getElementAt(index);
            try {
                DaoUsuarios.eliminar(usuario);
            } catch (SQLException ex) {
                Logger.getLogger(Index.class.getName()).log(Level.SEVERE, null, ex);
            }
            finally{
                jTextField1.setText(" ");
                jTextField2.setText(" ");
                jLabel5.setText(" ");
                try{
                    this.crearModelo();
                } catch (SQLException ex){
                    Logger.getLogger(Index.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        else{
            int index = jList1.getSelectedIndex();
            Usuarios usuario = (Usuarios)modelo.getElementAt(index);
            jTextField1.setText(usuario.getNombreUsuario());
            jTextField2.setText(usuario.getContrasena());
            jLabel5.setText(""+usuario.getId());
        }
    }//GEN-LAST:event_jList1MouseClicked

