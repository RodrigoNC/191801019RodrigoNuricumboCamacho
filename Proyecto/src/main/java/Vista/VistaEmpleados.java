    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombreEmpleado = jTextField1.getText();
        String apellidoPaterno = jTextField2.getText();
        String apellidoMaterno = jTextField3.getText();
        String genero = jTextField4.getText();
        int edad = Integer.parseInt(jTextField5.getText());
        String cargo = jTextField6.getText();
        int sueldo = Integer.parseInt(jTextField7.getText());
        String turno = jTextField8.getText();
        Empleado empleado = new Empleado(cargo, sueldo, turno, nombreEmpleado, apellidoPaterno, apellidoMaterno, genero, edad);
        try {
            DaEmpleados.insertar(empleado);
        } catch (SQLException ex) {
            Logger.getLogger(VistaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
            jTextField8.setText("");
            try{
                    this.crearModelo();
                    JOptionPane.showMessageDialog(null, "Empleado insertado correctamente");
            } catch (SQLException ex){
                    Logger.getLogger(VistaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Error: Empleado no insertado");
                }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(evt.getClickCount()==2){
            int index = jList1.getSelectedIndex();
            Empleado empleado = (Empleado)modelo.getElementAt(index);
            try {
                DaEmpleados.eliminar(empleado);
            } catch (SQLException ex) {
                Logger.getLogger(VistaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
            }
            finally{
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
                jTextField8.setText("");
                jLabel10.setText("");
                try{
                    this.crearModelo();
                    JOptionPane.showMessageDialog(null, "Empleado eliminado correctamente");
                } catch (SQLException ex){
                    Logger.getLogger(VistaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Error: El empleado no se eliminÃ³");
                }
            }
        }
        else{
            int index = jList1.getSelectedIndex();
            Empleado empleado = (Empleado)modelo.getElementAt(index);
            jTextField1.setText(empleado.getNombre());
            jTextField2.setText(empleado.getApellidoPaterno());
            jTextField3.setText(empleado.getApellidoMaterno());
            jTextField4.setText(empleado.getGenero());
            jTextField5.setText(""+empleado.getEdad());
            jTextField6.setText(empleado.getCargo());
            jTextField7.setText(""+empleado.getSueldo());
            jTextField8.setText(empleado.getTurno());
            jLabel10.setText(""+empleado.getRfc());
        }
    }//GEN-LAST:event_jButton1MouseClicked

